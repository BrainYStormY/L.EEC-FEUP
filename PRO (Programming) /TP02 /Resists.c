#include<stdio.h>
int main(){
    float R1, R2, Rs, Rp;
    printf("R1 (Ohm)?\n");
    scanf("%f",&R1);
    printf("R2 (Ohm)?\n");
    scanf("%f",&R2);
    Rs=R1+R2;
    Rp=(R1*R2)/(R1+R2);
    printf("Equivalent resistance in series: %.4f Ohm\nEquivalent resistance in parallel: %.4f Ohm\n",Rs,Rp);
}
